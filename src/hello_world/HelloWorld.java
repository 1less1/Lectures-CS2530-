package hello_world;

// A "class" is a 'recipe' for an object. It provides the state and
// behavior for instances of that class. It will be the "fundamental unit"
// that we work with.
public class HelloWorld {

    // This class contains only a special main method that defines what
    // we should do if we 'run' the program.
    public static void main(String[] args) {

        // We'll send a 'println' message to the 'out' object that is
        // defined in the class System.
        System.out.println("Hello, world!");
    }
}
